void Text::displayString(const char *string, uint8_t color, uint16_t x, uint16_t y, bool upscale){
	int len = strlen(string);
	for (int i = 0; i < len; i++){
		char c = string[i];
		const unsigned char *sym = 0;
		
		if (c >= 'a' && c <= 'z'){
			c -= 0x20;
		}
		
		if (c >= 'A' && c <= 'Z'){
			sym = symbols[c-'A'];
		}
		
		if (c >= '0' && c <= '9'){
			sym = symbols[c-'0'+26];
		}
		
		if (c == '-'){
			sym = minus;
		}
		
		if (c == '+'){
			sym = plus;
		}
		
		if (c == '\''){
			sym = quote;
		}
		
		if (c == '.'){
			sym = dot;
		}
		
		if (c == ','){
			sym = comma;
		}
		
		if (c == '_'){
			sym = ground;
		}
		
		if (c == '~'){
			sym = tilda;
		}
		
		if (c == '/'){
			sym = slash;
		}
		
		if (sym){
			Engine::displaySpriteBitMask(sym, color, x, y, 1, 12, 0, upscale);
		}
		
		x += upscale ? 16 : 8;
	}
}





